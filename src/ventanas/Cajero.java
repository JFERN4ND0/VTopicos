    private void jtnproductoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jtnproductoKeyTyped
        // TODO add your handling code here:
        String producto = jtnproducto.getText();
        if (evt.getKeyChar() == KeyEvent.VK_ENTER) {
            if (!producto.equals("")) {
                Object[] fila = new Object[2];
                fila[0] = producto;
                fila[1] = 16.00;

                cuenta = cuenta + 16.00;
                jltotal.setText("TOTAL:   $" + cuenta);

                model.addRow(fila);
            } else {
                JOptionPane.showMessageDialog(null, "INTRODUCE UN PRODUCTO");
            }

            jtnproducto.setText("");
        } else if (evt.getKeyChar() == KeyEvent.VK_END) {
            if (!producto.equals("")) {
                jlprecioi.setText("$ 16.00");
            } else {
                JOptionPane.showMessageDialog(null, "INTRODUCE UN PRODUCTO");
            }
        }
    }//GEN-LAST:event_jtnproductoKeyTyped

